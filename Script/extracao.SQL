        ' '                                                  AS TIPO,
        ' '                                                  AS COR,
        ' '                                                  AS EMBALAGEM,
        1                                                    AS QUERY_INFO
    FROM PRODDTA.F0901 A
    JOIN saldos_f0902 C         ON (A.GMAID = C.GBAID)
    LEFT JOIN PRODCTL.F0005 D    ON (D.DRSY='09' AND D.DRRT='09' AND TRIM(D.DRKY)=TRIM(A.GMR009))
    LEFT JOIN PRODCTL.F0005 E    ON (E.DRSY='09' AND E.DRRT='10' AND TRIM(E.DRKY)=TRIM(A.GMR010))
    LEFT JOIN PRODDTA.F0006 F    ON (F.MCMCU = A.GMMCU)
    LEFT JOIN PRODCTL.F0005 R49  ON (R49.DRSY='00' AND R49.DRRT='49' AND TRIM(F.MCRP49)=TRIM(R49.DRKY))
    LEFT JOIN PRODCTL.F0005 R50  ON (R50.DRSY='00' AND R50.DRRT='50' AND TRIM(F.MCRP50)=TRIM(R50.DRKY))
