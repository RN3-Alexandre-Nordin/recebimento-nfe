        ABS(C.VALOR_PERIODO) > 0
        /* Evite filtros que removam contas sem rateio; mantenha apenas os que vocÃª realmente precisa */
        AND NVL(TRIM(D.DRDL01), 'Â§Â§') NOT IN ('Faturamento Bruto','(-) IMPOSTOS FATURADO')
        AND NVL(TRIM(E.DRDL01), 'Â§Â§') <> 'DevoluÃ§Ãµes'
        AND TRIM(GMOBJ || GMSUB) NOT IN ('311210001','311210004','311210003')
        --AND TRIM(GMOBJ || GMSUB) = ('510120005')
        --AND GLDOC = 684383
