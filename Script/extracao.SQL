WITH params AS (
    SELECT
        DATE '2016-01-01' AS DT_INI,
        DATE '2016-02-01' AS DT_FIM,
        116001 AS JUL_INI,
        116031 AS JUL_FIM,
        (EXTRACT(YEAR FROM DATE '2016-01-01') - 1900) AS FY_CYY,
        EXTRACT(MONTH FROM DATE '2016-01-01') AS PERIODO_FISCAL
    FROM DUAL
),
dim_item AS (
    SELECT
        i.IMLITM,
        i.IMSRP1,
        i.IMSRP2,
        i.IMSRP3,
        i.IMSRP4,
        i.IMSRP5,
        TRIM(s1.DRDL01) AS LINHA,
        TRIM(s2.DRDL01) AS TAMANHO,
        TRIM(s3.DRDL01) AS TIPO,
        TRIM(s4.DRDL01) AS COR,
        TRIM(s5.DRDL01) AS EMBALAGEM
    FROM PRODDTA.F4101 i
    LEFT JOIN PRODCTL.F0005 s1 ON (s1.DRSY='41' AND s1.DRRT='S1' AND TRIM(i.IMSRP1)=TRIM(s1.DRKY))
    LEFT JOIN PRODCTL.F0005 s2 ON (s2.DRSY='41' AND s2.DRRT='S2' AND TRIM(i.IMSRP2)=TRIM(s2.DRKY))
    LEFT JOIN PRODCTL.F0005 s3 ON (s3.DRSY='41' AND s3.DRRT='S3' AND TRIM(i.IMSRP3)=TRIM(s3.DRKY))
    LEFT JOIN PRODCTL.F0005 s4 ON (s4.DRSY='41' AND s4.DRRT='S4' AND TRIM(i.IMSRP4)=TRIM(s4.DRKY))
    LEFT JOIN PRODCTL.F0005 s5 ON (s5.DRSY='41' AND s5.DRRT='S5' AND TRIM(i.IMSRP5)=TRIM(s5.DRKY))
),
saldos_f0902 AS (
    SELECT
        c.GBAID,
        CASE p.PERIODO_FISCAL
            WHEN 1  THEN NVL(c.GBAN01, 0)
            WHEN 2  THEN NVL(c.GBAN02, 0)
            WHEN 3  THEN NVL(c.GBAN03, 0)
            WHEN 4  THEN NVL(c.GBAN04, 0)
            WHEN 5  THEN NVL(c.GBAN05, 0)
            WHEN 6  THEN NVL(c.GBAN06, 0)
            WHEN 7  THEN NVL(c.GBAN07, 0)
            WHEN 8  THEN NVL(c.GBAN08, 0)
            WHEN 9  THEN NVL(c.GBAN09, 0)
            WHEN 10 THEN NVL(c.GBAN10, 0)
            WHEN 11 THEN NVL(c.GBAN11, 0)
            WHEN 12 THEN NVL(c.GBAN12, 0)
            WHEN 13 THEN NVL(c.GBAN13, 0)
            WHEN 14 THEN NVL(c.GBAN14, 0)
            ELSE 0
        END AS VALOR_PERIODO
    FROM PRODDTA.F0902 c
    CROSS JOIN params p
    WHERE c.GBLT = 'AA'
      AND c.GBFY = p.FY_CYY
),
base AS (
